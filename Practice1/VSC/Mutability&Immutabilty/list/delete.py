#Delete operation
#remove(),clear(),pop()


#pop() - it'll removes the last element from the list
name=['Arjun','Aravind','Aarthi','Anshitha','Arun','Arha','Amith','Aravind','Aarthi']
name.pop()
print(name)
#['Arjun', 'Aravind', 'Aarthi', 'Anshitha', 'Arun', 'Arha', 'Amith', 'Aravind']

#remove() - to select, which element wants to remove from the list
name1=['Arjun','Aravind','Aarthi','Anshitha','Arun','Arha','Amith','Aravind','Aarthi']
name1.remove("Arha")
print(name1)
#['Arjun', 'Aravind', 'Aarthi', 'Anshitha', 'Arun', 'Amith', 'Aravind', 'Aarthi']

#claer() - it'll clear all the values from the list
name2=['Arjun','Aravind','Aarthi','Anshitha','Arun','Arha','Amith','Aravind','Aarthi']
name2.clear()
print(name2)