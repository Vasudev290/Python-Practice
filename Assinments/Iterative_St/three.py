#3.Program to calculate the sum of the first 10 natural numbers using for Loop- forloop and While loop?

#forloop
num=0
for i in range(1,11):
    num += i
print("first 10 natural numbers for loop:",num)

#whileloop
num1=0
i1=1
while i1 <= 12:
    num1 += i1
    i1 += 1
print("first 12 natural numbers in whileloop:",num1)